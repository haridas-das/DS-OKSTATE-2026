COLORS = {
    '95': '#d0e4f7',
    '90': '#a8cce8',
    '80': '#6aaed6',
    '50': '#2171b5',
}

def get_true_from_dfm(ew_start, ew_end, dates_in_csv):
    values = dfm[dfm['epiweek'].between(ew_start, ew_end)][selected_state_name].values
    dates  = pd.to_datetime(dates_in_csv)
    n      = min(len(values), len(dates))
    return pd.Series(values[:n], index=dates[:n])

def plot_bpi(csv_path, title, true_series=None, save_path=None):
    df_plot = pd.read_csv(csv_path, parse_dates=['date'])
    fig, ax = plt.subplots(figsize=(18, 5))

    for level in ['95', '90', '80', '50']:
        ax.fill_between(df_plot['date'],
                        df_plot[f'lower_{level}'],
                        df_plot[f'upper_{level}'],
                        color=COLORS[level], alpha=0.9, label=f'{level}% PI')

    ax.plot(df_plot['date'], df_plot['pred'],
            color='red', linewidth=2, marker='o', markersize=3, label='Forecast')

    if true_series is not None:
        common = df_plot['date'][df_plot['date'].isin(true_series.index)]
        ax.plot(common, true_series.reindex(common).values,
                color='black', linewidth=1.5, marker='s', markersize=3,
                label='True cases')

    ax.set_title(title, fontsize=13)
    ax.set_xlabel('Date')
    ax.set_ylabel('Dengue cases')
    ax.legend(frameon=False, ncol=6, fontsize=9)
    ax.xaxis.set_major_locator(plt.MaxNLocator(20))
    plt.xticks(rotation=45, ha='right')
    plt.tight_layout()

    if save_path:
        fig.savefig(save_path, dpi=150, bbox_inches='tight')
        print(f'✅ Plot saved: {save_path}')
    plt.show()
    plt.close(fig)

