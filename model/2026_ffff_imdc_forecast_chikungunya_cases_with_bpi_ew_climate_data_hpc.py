def bpi_cnn_lstm_ensemble(X_train_3d, y_train_1d, X_test_3d,
                           point_forecast,              # ← ADD
                           filters_list, kernel_size, learning_rate,
                           n_steps, n_features,
                           B=50, block_size=4, epochs=50,
                           quantiles=(0.025, 0.05, 0.10, 0.25,
                                      0.75, 0.90, 0.95, 0.975)):

    """
    BPI using multiple filters — runs B bootstrap resamples for EACH filter,
    then pools all predictions before computing quantiles.
    With filters_list=[256,512] and B=50 → 100 total predictions per week.
    """
    n        = len(X_train_3d)
    nob      = n - block_size + 1
    n_blocks = int(np.ceil(n / block_size))
    H        = len(X_test_3d)

    all_boot_rows = []

    for filters in filters_list:
        print(f"    filter={filters}: running {B} bootstrap resamples ...")
        boot_matrix = np.zeros((B, H))

        for b in range(B):
            # Randomly resample the training data by selecting consecutive time blocks.
            # This creates different training datasets while preserving temporal dependence.
            starts  = np.random.randint(0, nob, size=n_blocks)
            idx     = np.concatenate(
                          [np.arange(s, s + block_size) for s in starts])[:n]
            X_boot  = X_train_3d[idx]
            y_boot  = y_train_1d[idx]
            # Train a new CNN-LSTM model on each bootstrap sample.
            # Different bootstrap samples and random model initialization produce
            # multiple possible forecasts used to quantify prediction uncertainty.
            m = build_cnn_lstm_model(
                    filters, kernel_size, learning_rate, n_steps, n_features)
            m.fit(X_boot, y_boot, epochs=epochs,
                  validation_split=0.1, batch_size=5, verbose=0)

            boot_matrix[b] = m.predict(X_test_3d, verbose=0).flatten()[:H]

        all_boot_rows.append(boot_matrix)

    pooled = np.vstack(all_boot_rows)
    print(f"    pooled bootstrap matrix: {pooled.shape} "
          f"({len(filters_list)} filters × {B} resamples)")

    # # center bootstrap distribution on Cell 1 point forecast and boot strapping PI
    # boot_mean = np.mean(pooled, axis=0)
    # residuals = pooled - boot_mean
    # centered  = point_forecast + residuals
    # centered  = np.clip(centered, 0, None)

    # bpi = {}
    # bpi['pred'] = np.clip(point_forecast, 0, None)   # ← Cell 1 as point forecast
    # for q in quantiles:
    #     bpi[q] = np.percentile(centered, q * 100, axis=0)
    # return bpi


    # Combined point forecast: Cell 1 ensemble + bootstrap mean
    # alpha=0.5: equal weight — uses Cell 1 residual knowledge + bootstrap stability

    # Combine the original point forecast with the average bootstrap forecast.
    # alpha=0.5 gives equal weight to the original model forecast and bootstrap ensemble mean.

    # boot_mean  = np.mean(pooled, axis=0)
    # alpha      = 0.5
    # final_pred = alpha * point_forecast + (1 - alpha) * boot_mean
    # final_pred = np.clip(final_pred, 0, None)

    # bpi = {}
    # bpi['pred'] = final_pred                  # ← combined point forecast
    # for q in quantiles:
    #     bpi[q] = np.clip(
    #         np.percentile(pooled, q * 100, axis=0), 0, None)
    # return bpi

    # Combined point forecast: Cell 1 ensemble + bootstrap mean
    # alpha=0.5: equal weight — uses Cell 1 residual knowledge + bootstrap stability
    boot_mean  = np.mean(pooled, axis=0)
    alpha      = 0.5
    final_pred = alpha * point_forecast + (1 - alpha) * boot_mean
    final_pred = np.clip(final_pred, 0, None)

    # Recentre bootstrap deviations on final_pred
    # → guarantees intervals always bracket final_pred by construction
    boot_residuals = pooled - boot_mean       # (100, H) deviations; mean = 0
    centered       = final_pred + boot_residuals  # shift to final_pred; mean = final_pred

    bpi = {}
    bpi['pred'] = final_pred                  # ← combined point forecast
    for q in quantiles:
        bpi[q] = np.clip(
            np.percentile(centered, q * 100, axis=0), 0, None)
    return bpi



